const int NUM_BANKS = sizeof(banks) / sizeof(banks[0]);

uint16_t getBankPatternWord(uint8_t bankIndex, uint8_t channel, uint8_t bar) {
  // Direct flash read: avoids copying the full banks table to SRAM.
  return pgm_read_word(&(banks[bankIndex].patterns[channel][bar]));
}

void getBankName(uint8_t bankIndex, char* outName, size_t outSize) {
  if (outSize == 0) return;

  // Wichtig: Der Name liegt in PROGMEM. Hier darf nicht einfach ein char*-Pointer aus dem RAM
  // verwendet werden, sondern es muss mit strncpy_P() direkt aus dem Flash kopiert werden.
  // Das vermeidet wirre Anzeigeausgaben durch falsche Pointer- oder Speicherzugriffe.
  strncpy_P(outName, banks[bankIndex].name, outSize - 1);
  outName[outSize - 1] = '\0';
}
